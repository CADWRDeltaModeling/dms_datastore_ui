def read_hist_wateryear_types(fpath: str) -> pd.DataFrame:
    """Read historical Sacramento Valley water year types from a WSIHIST text file.

    The expected source is the fixed-width table from CDEC at
    http://cdec.water.ca.gov/reportapp/javareports?name=WSIHIST — the upper
    table, stopping before the min/average rows at the bottom.

    Parameters
    ----------
    fpath : str or path-like
        Path to the WSIHIST text file.

    Returns
    -------
    pandas.DataFrame
        Columns include ``wy`` (int), ``sac_yrtype`` (str: W/AN/BN/D/C), and
        SJR equivalents.  Suitable for passing directly to
        :func:`build_d1641_references`.
    """
    df = pd.read_fwf(
        fpath,
        header=None,
        names=[
            "wy",
            "sac_oct_mar",
            "sac_apr_jul",
            "sac_wysum",
            "sac_index",
            "sac_yrtype",
            "sjr_oct_mar",
            "sjr_apr_jul",
            "sjr_wysum",
            "sjr_index",
            "sjr_yrtype",
        ],
        skiprows=14,
    )
    return df


def _read_regulations(fpath: str, df_wyt: pd.DataFrame) -> pd.DataFrame:
    """Build an irregular regulation step-function from a D-1641 lookup CSV.

    Parameters
    ----------
    fpath : str or path-like
        D-1641 CSV with columns ``location``, ``wyt``, ``month``, ``date``,
        ``val``; lines starting with ``#`` are comments.
    df_wyt : pandas.DataFrame
        Water year type table with at minimum columns ``wy`` and
        ``sac_yrtype``.

    Returns
    -------
    pandas.DataFrame
        DatetimeIndex, columns ``value`` (float) and ``location`` (str).
        Each row is a step-function breakpoint; the regulation value is
        constant from that timestamp until the next breakpoint for the same
        location.
    """
    df = pd.read_csv(fpath, comment="#")
    df[["month", "date"]] = df[["month", "date"]].astype(int)
    df_wy = df_wyt[df_wyt["sac_yrtype"].notnull()].copy()
    yr_start = int(df_wy["wy"].min())
    yr_end = int(df_wy["wy"].max())
    locs = df["location"].unique()
    dfs = []
    for loc in locs:
        timestamps = []
        regs = []
        for wy in range(yr_start, yr_end + 1):
            wyt_rows = df_wyt[df_wyt["wy"] == wy]["sac_yrtype"]
            if wyt_rows.empty:
                continue
            wyt = wyt_rows.iloc[0]
            mask = (df["wyt"] == wyt) & (df["location"] == loc)
            for _, row in df[mask].iterrows():
                mo = int(row["month"])
                yr_cal = wy - 1 if mo >= 10 else wy
                t_start = pd.Timestamp(
                    f"{yr_cal:d}-{mo:02d}-{int(row['date']):02d}"
                )
                timestamps.append(t_start)
                regs.append(float(row["val"]))
        if timestamps:
            dfs.append(
                pd.DataFrame(
                    {"value": regs, "location": loc},
                    index=pd.DatetimeIndex(timestamps),
                )
            )
    if not dfs:
        return pd.DataFrame(
            columns=["value", "location"],
            index=pd.DatetimeIndex([]),
        )
    return pd.concat(dfs)


def _generate_regulation_timeseries(
    df_reg: pd.DataFrame,
    location: str,
    start_date,
    end_date,
    freq: str = "D",
) -> pd.DataFrame:
    """Expand an irregular regulation step-function to a dense regular series.

    This is a refactored version of ``generate_regulation_timeseries`` from
    ``dsm2_calsim_analysis.utilities``, taking explicit date bounds instead of
    deriving them from an observed-data DataFrame (resolving the upstream FIXME).

    Parameters
    ----------
    df_reg : pandas.DataFrame
        Output of :func:`_read_regulations`.
    location : str
        DSM2 station ID to extract, e.g. ``"RSAC092"``.
    start_date, end_date : date-like
        Inclusive start/end of the output time series.
    freq : str
        Pandas offset alias for the output grid.  Default ``"D"`` (daily).

    Returns
    -------
    pandas.DataFrame
        Index: DatetimeIndex named ``"datetime"``, single column ``"value"``.
    """
    start = pd.Timestamp(start_date)
    end = pd.Timestamp(end_date)

    df_loc = df_reg[df_reg["location"] == location].sort_index()
    # Expand search window so breakpoints just outside start/end are captured.
    window_mask = (
        (df_loc.index >= (start - pd.Timedelta("400 days")))
        & (df_loc.index <= (end + pd.Timedelta("400 days")))
    )
    df_win = df_loc[window_mask]

    times: list = []
    values: list = []
    for i in range(len(df_win) - 1):
        row_i = df_win.iloc[i]
        row_next = df_win.iloc[i + 1]
        segment = pd.date_range(
            start=row_i.name, end=row_next.name, freq=freq, inclusive="left"
        )
        times.extend(segment.tolist())
        values.extend([row_i["value"]] * len(segment))

    # Extend from the last breakpoint to end_date so the series covers the
    # full requested period (the loop above always omits the final row).
    if len(df_win) > 0:
        last_row = df_win.iloc[-1]
        tail_start = last_row.name
        if tail_start <= end:
            tail = pd.date_range(start=tail_start, end=end, freq=freq, inclusive="left")
            if len(tail) > 0:
                # Avoid double-counting if tail_start was already the boundary of
                # the previous segment.
                t0 = pd.Timestamp(times[-1]) + pd.tseries.frequencies.to_offset(freq) if times else tail_start
                tail = tail[tail >= t0]
                times.extend(tail.tolist())
                values.extend([last_row["value"]] * len(tail))

    if not times:
        return pd.DataFrame(
            {"value": pd.Series(dtype=float)},
            index=pd.DatetimeIndex([], name="datetime"),
        )

    df_ts = pd.DataFrame(
        {"value": values},
        index=pd.DatetimeIndex(times, name="datetime"),
    )
    return df_ts.loc[start:end]


