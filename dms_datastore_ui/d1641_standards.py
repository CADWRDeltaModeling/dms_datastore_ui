"""D-1641 EC and chloride regulatory standards for dms_datastore_ui catalog.

Generates time series of D-1641 regulatory standards (State Water Resources
Control Board Decision 1641) for key Delta compliance stations. Standards are
driven by Sacramento Valley water year types read from a historical WSIHIST text
file (http://cdec.water.ca.gov/reportapp/javareports?name=WSIHIST).

Stations covered
----------------
emm  Emmaton (USBR, CDEC D22A) — corresponds to DSM2 RSAC092
jer  Jersey Point (USBR, CDEC D15) — corresponds to DSM2 RSAN018
rsl  Rock Slough at Contra Costa Canal — corresponds to DSM2 ROLD024

Standards generated
-------------------
D1641_AG_WI    14-day running avg EC, Agricultural W&I Delta (microS/cm)
D1641_FWS_SJR  14-day running avg EC, Fish & Wildlife Service SJR (microS/cm)
D1641_MI_250   Daily chloride, Municipal & Industrial intake (mg/L)

The Rock Slough MI 250 Cl standard (250 mg/L constant) can be displayed on a
dual Y-axis alongside observed EC via the multi_y behaviour enabled in
DatastoreUIMgr.get_convertible_unit_groups. The rsl_ec_to_cl regression bundled
here can be used separately to derive observed chloride from observed EC.
"""

