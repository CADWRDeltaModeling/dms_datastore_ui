def rsl_ec_to_cl(ec):
    """Convert Rock Slough EC (µS/cm) to chloride (mg/L) via a piecewise regression.

    The regression is::

        Cl = max(EC × 0.15 − 12.0,  EC × 0.285 − 50)

    Source: ``dsm2_calsim_analysis.nbplot.calsim_dsm2_analysis.rsl_ec_to_cl``

    This function is **not** used in D-1641 standard generation; it is provided
    here so callers can derive observed chloride from observed EC at Rock Slough
    as a math/derived catalog reference.

    Parameters
    ----------
    ec : float, array-like, or pandas.Series
        Electrical conductivity in µS/cm.

    Returns
    -------
    float, numpy.ndarray, or pandas.Series
        Chloride in mg/L.
    """
    if isinstance(ec, pd.Series):
        return ec.apply(lambda x: max(x * 0.15 - 12.0, x * 0.285 - 50))
    ec_arr = np.asarray(ec, dtype=float)
    return np.maximum(ec_arr * 0.15 - 12.0, ec_arr * 0.285 - 50)


