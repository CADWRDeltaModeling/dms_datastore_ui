#: Sacramento Valley Water Year Index thresholds (MAF) per D-1641.
_SVI_THRESHOLDS = [
    ("W",  9.2),   # Wet:          index >= 9.2
    ("AN", 7.8),   # Above Normal: index >  7.8
    ("BN", 6.5),   # Below Normal: index >  6.5
    ("D",  5.4),   # Dry:          index >  5.4
]                  # Critical:     index <= 5.4


def _classify_svi(index_maf: float) -> str:
    """Classify a Sacramento Valley index value (MAF) into a WYT code.

    D-1641 thresholds (index in million acre-feet):
        W  \u2265 9.2
        AN > 7.8, < 9.2   (strictly greater than 7.8)
        BN > 6.5, \u2264 7.8  (strictly greater than 6.5)
        D  > 5.4, \u2264 6.5  (strictly greater than 5.4)
        C  \u2264 5.4
    """
    if index_maf >= 9.2:  return "W"
    if index_maf >  7.8:  return "AN"
    if index_maf >  6.5:  return "BN"
    if index_maf >  5.4:  return "D"
    return "C"


def _no_wsi_result(wy: int) -> dict:
    """Return a 'no forecast data' placeholder dict."""
    return {
        "wy": wy,
        "wyt": None,
        "svi_50pct": None,
        "forecast_date": None,
        "is_final": False,
        "source": "none",
    }


def _parse_wsi_text(text: str, expected_wy: int) -> dict:
    """Parse a plain-text CDEC WSI report and return the current WYT.

    Extracts the Sacramento Valley SVI at 50 % exceedance from the most
    recent forecast row in the report.

    Parameters
    ----------
    text : str
        Plain text extracted from the CDEC WSI ``<pre>`` block.
    expected_wy : int
        The water year number we expect (used as fallback in the result).

    Returns
    -------
    dict
        Keys: ``wy``, ``wyt``, ``svi_50pct``, ``forecast_date``,
        ``is_final``, ``source``.
    """
    import re

    # Header line: "2026 Water Year Forecast as of May 1, 2026"
    header_m = re.search(
        r'(\d{4})\s+Water Year Forecast as of\s+'
        r'((?:Jan|Feb|Mar|Apr|May|Jun|Jul|Aug|Sep|Oct|Nov|Dec)\w*)'
        r'\s+(\d+),\s*(\d{4})',
        text,
    )
    if not header_m:
        logger.warning("WSI parse: 'Water Year Forecast as of' header not found")
        return _no_wsi_result(expected_wy)

    wy = int(header_m.group(1))
    f_month = header_m.group(2)[:3]
    f_day   = int(header_m.group(3))
    f_year  = int(header_m.group(4))
    try:
        forecast_date = pd.Timestamp(f"{f_year}-{f_month}-{f_day}")
    except Exception:
        return _no_wsi_result(expected_wy)

    # Locate the Sacramento Valley SVI section
    svi_start = text.find("SACRAMENTO VALLEY WATER YEAR TYPE INDEX")
    if svi_start == -1:
        logger.warning("WSI parse: Sacramento Valley SVI section not found")
        return _no_wsi_result(expected_wy)

    svi_text = text[svi_start:]
    notes_start = svi_text.find("Index =")
    if notes_start > 0:
        svi_text = svi_text[:notes_start]

    # Match forecast rows — use [ \t]+ (not \s+) to stay within one line.
    # The 25 % and 10 % columns wrap to the next line in the WSI report, but
    # the first four columns (99 %, 90 %, 75 %, 50 %) are on the date line.
    rows = re.findall(
        r'((?:Jan|Feb|Mar|Apr|May|Jun|Jul|Aug|Sep|Oct|Nov|Dec)'
        r'\s+\d+,\s*\d{4})'
        r'[ \t]+([\d.]+)[ \t]+([\d.]+)[ \t]+([\d.]+)[ \t]+([\d.]+)',
        svi_text,
    )
    if not rows:
        logger.warning("WSI parse: no SVI forecast rows found")
        return _no_wsi_result(expected_wy)

    # Last row = most recent forecast; tuple = (date, 99%, 90%, 75%, 50%)
    try:
        svi_50 = float(rows[-1][4])
    except (IndexError, ValueError):
        return _no_wsi_result(expected_wy)

    wyt = _classify_svi(svi_50)
    is_final = forecast_date.month >= 5   # May 1 or later is the final D-1641 type

    logger.debug(
        "WSI WY %d: %s, SVI 50%%=%.2f → %s (final=%s)",
        wy, forecast_date.date(), svi_50, wyt, is_final,
    )
    return {
        "wy": wy,
        "wyt": wyt,
        "svi_50pct": svi_50,
        "forecast_date": forecast_date,
        "is_final": is_final,
        "source": "wsi_live",
    }


def _fetch_wsi_live(expected_wy: int) -> dict:
    """Fetch and parse the live CDEC WSI page."""
    try:
        import urllib.request
        from bs4 import BeautifulSoup

        url = "http://cdec.water.ca.gov/reportapp/javareports?name=WSI"
        req = urllib.request.Request(
            url, headers={"User-Agent": "dms_datastore_ui/wyt_fetch"}
        )
        with urllib.request.urlopen(req, timeout=15) as resp:
            html = resp.read().decode("utf-8", errors="replace")

        soup = BeautifulSoup(html, "html.parser")
        pre = soup.find("pre")
        text = pre.get_text() if pre else soup.get_text()
    except Exception as exc:
        logger.warning("WSI live fetch failed: %s", exc)
        return _no_wsi_result(expected_wy)

    return _parse_wsi_text(text, expected_wy)


def fetch_current_wyt(cache_dir: str | None = None) -> dict:
    """Return the current water year type from the CDEC WSI, with monthly caching.

    The CDEC Water Supply Index (WSI) is issued on the 1st of each month from
    December through May.  The final D-1641 type is the **May 1 50 % exceedance
    forecast**.  For October and November, no forecast has been issued yet and
    this function returns a ``'none'`` result.

    Caching uses ``diskcache`` (already a dms_datastore_ui dependency).  The
    cache key changes each calendar month so new forecasts are picked up
    automatically; entries also expire after 30 days.

    Parameters
    ----------
    cache_dir : str, optional
        Override the cache directory.  Defaults to
        ``~/.dms_datastore_ui/wsi_cache``.

    Returns
    -------
    dict with keys:
        ``wy`` (int), ``wyt`` (str or None), ``is_final`` (bool),
        ``forecast_date`` (Timestamp or None), ``svi_50pct`` (float or None),
        ``source`` ('wsi_live' | 'wsi_cached' | 'none')
    """
    import diskcache

    today = pd.Timestamp.now()
    # WY n covers Oct(n-1) – Sep(n); e.g. WY 2027 starts Oct 1, 2026
    current_wy = today.year + 1 if today.month >= 10 else today.year

    # Oct and Nov: no WSI forecast issued for this WY yet
    if today.month in (10, 11):
        return _no_wsi_result(current_wy)

    cache_path = cache_dir or str(Path.home() / ".dms_datastore_ui" / "wsi_cache")
    key = f"wsi_wyt_{today.year}_{today.month:02d}"

    cache = None
    try:
        cache = diskcache.Cache(cache_path)
        cached = cache.get(key)
        if cached is not None:
            result = dict(cached)
            result["source"] = "wsi_cached"
            return result
    except Exception as exc:
        logger.warning("WSI cache read failed (%s): %s", cache_path, exc)

    result = _fetch_wsi_live(current_wy)

    if cache is not None:
        try:
            cache.set(key, result, expire=60 * 60 * 24 * 30)  # 30-day TTL
        except Exception as exc:
            logger.warning("WSI cache write failed: %s", exc)

    return result


