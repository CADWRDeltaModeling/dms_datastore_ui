def build_d1641_references(
    wyt_file: str,
    standards: Optional[List[D1641StandardSpec]] = None,
    station_id_map: Optional[Dict[str, str]] = None,
    station_coords: Optional[Dict[str, Tuple[float, float]]] = None,
) -> list:
    """Build DataReference objects for D-1641 regulatory standards.

    Each reference is backed by a
    :class:`~dvue.catalog.CallableDataReferenceReader` and carries metadata
    compatible with the ``DatastoreUIMgr`` catalog schema (``station_id``,
    ``subloc``, ``param``, ``unit``, ``agency``, ``geometry``, ``min_year``,
    ``max_year``).  The references use the same ``station_id`` values as the
    corresponding observed stations, enabling direct side-by-side comparison in
    the UI.

    Parameters
    ----------
    wyt_file : str or path-like
        Path to a WSIHIST-format historical water year type text file from CDEC.
    standards : list of D1641StandardSpec, optional
        Standards to generate.  Defaults to :data:`DEFAULT_STANDARDS`.
    station_id_map : dict, optional
        Override mapping from DSM2 location ID to dms_datastore
        ``station_id``.  Defaults to :data:`DSM2_TO_DATASTORE_ID`.
    station_coords : dict, optional
        Override UTM Zone 10N ``(x, y)`` coords keyed by ``station_id``.
        Defaults to :data:`STATION_COORDS`.

    Returns
    -------
    list of dvue.catalog.DataReference
        One reference per (standard × DSM2 location) present in
        ``station_id_map``.
    """
    from dvue.catalog import CallableDataReferenceReader, DataReference

    if standards is None:
        standards = DEFAULT_STANDARDS
    if station_id_map is None:
        station_id_map = DSM2_TO_DATASTORE_ID
    if station_coords is None:
        station_coords = STATION_COORDS

    df_wyt = read_hist_wateryear_types(wyt_file)
    start_wy = int(df_wyt["wy"].min())
    end_wy   = int(df_wyt["wy"].max())
    # Water year n covers Oct(n-1) through Sep(n).
    start_date = pd.Timestamp(f"{start_wy - 1}-10-01")

    # The standards always extend through the current water year.
    # max_year on the DataReference reflects this dynamic range.
    today = pd.Timestamp.now()
    current_wy = today.year + 1 if today.month >= 10 else today.year
    catalog_max_year = max(end_wy, current_wy)

    data_dir = _data_dir()
    refs = []

    for spec in standards:
        csv_path = str(data_dir / spec.csv_filename)
        for dsm2_loc in spec.dsm2_locations:
            station_id = station_id_map.get(dsm2_loc)
            if station_id is None:
                logger.warning(
                    "D-1641 %s: no station_id mapping for DSM2 location %s — skipped",
                    spec.name,
                    dsm2_loc,
                )
                continue

            x, y = station_coords.get(station_id, (None, None))
            geometry = Point(x, y) if x is not None else None
            station_name = STATION_NAMES.get(station_id, station_id)

            # _end is NOT captured here — the loader computes it dynamically so
            # it always extends to cover the current water year.
            def _make_loader(
                _wyt_file=wyt_file,
                _csv_path=csv_path,
                _dsm2_loc=dsm2_loc,
                _scale=spec.unit_scale,
                _start=start_date,
            ):
                def _load():
                    _df_wyt = read_hist_wateryear_types(_wyt_file)
                    hist_end_wy = int(_df_wyt["wy"].max())

                    # Dynamically extend to the current water year using the
                    # monthly-cached CDEC WSI forecast (fast; ~1 ms cache hit).
                    dynamic_end_wy = hist_end_wy
                    try:
                        cur = fetch_current_wyt()
                        cur_wy = cur.get("wy")
                        if cur_wy and cur_wy > hist_end_wy:
                            wyt_use = cur.get("wyt")
                            if wyt_use is None:
                                # Oct–Nov: no WSI forecast yet; use prev year as proxy.
                                prev = _df_wyt[_df_wyt["wy"] == cur_wy - 1]
                                wyt_use = prev.iloc[0]["sac_yrtype"] if not prev.empty else None
                            if wyt_use:
                                extra = pd.DataFrame(
                                    [{"wy": cur_wy, "sac_yrtype": wyt_use}]
                                )
                                _df_wyt = pd.concat([_df_wyt, extra], ignore_index=True)
                                dynamic_end_wy = cur_wy
                    except Exception:
                        pass  # Network/cache failure: stay within historical range

                    _end = pd.Timestamp(f"{dynamic_end_wy}-10-01")
                    df_reg = _read_regulations(_csv_path, _df_wyt)
                    df_reg = df_reg.copy()
                    df_reg["value"] = df_reg["value"] * _scale
                    df = _generate_regulation_timeseries(
                        df_reg, _dsm2_loc, _start, _end, freq="D"
                    )
                    # 0 in the regulation CSVs means "no standard in effect"
                    # (off-season).  Replace with NaN so the plotted line has
                    # gaps rather than a meaningless flat line at 0.
                    df["value"] = df["value"].replace(0.0, float("nan"))
                    return df
                return _load

            reader = CallableDataReferenceReader(_make_loader())

            ref = DataReference(
                source="d1641",
                reader=reader,
                name=f"{station_id}_{spec.name}",
                cache=False,   # always recalculate — current year WYT changes monthly
                station_id=station_id,
                subloc="",
                param=spec.name,
                unit=spec.unit,
                agency="swrcb",
                agency_id_dbase=dsm2_loc,  # DSM2 model station ID, e.g. "RSAC092"
                station_name=f"D-1641 {spec.name} at {station_name}",
                variable=spec.name,
                id=station_id,
                min_year=start_wy - 1,
                max_year=catalog_max_year,
                filename=None,
                x=x,
                y=y,
                geometry=geometry,
            )
            refs.append(ref)
            logger.debug(
                "Built D-1641 ref: %s → station_id=%s  param=%s  unit=%s",
                dsm2_loc,
                station_id,
                spec.name,
                spec.unit,
            )

    return refs
